#include <string.h>

char	*ft_strcat(char *s1, const char *s2)
{
	while (*s2)
	{
		while (*s1)
		{
			if (*s1 == '\0')
				*s1++ = *s2++;
			else
				s2++;
		}
	}
	return (s1);
}
