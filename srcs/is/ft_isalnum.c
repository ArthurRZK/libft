int		ft_isalnum(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (1);
	else if (c >= 'a' && c <= 'z')
		return (1);
	else if (c <= '9' && c >= '0')
		return (1);
	else
		return (0);
}
