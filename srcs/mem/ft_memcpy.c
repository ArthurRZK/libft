#include <string.h>

void				*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*s1;
	unsigned char	*s2;
	size_t			i;

	i = 0;
	s1 = (unsigned char *)dst;
	s2 = (unsigned char *)src;
	if (s1 == NULL || s2 == NULL)
		return (0);
	while (i < n)
		s1[i] = s2[i];
	return (s1);
}
